export type LanguageCode = keyof typeof translations;
export type TranslationKey = keyof (typeof translations)["en"];

// ── Dynamic translation cache (localStorage) ──────────────────────────────
const CACHE_PREFIX = "polyglot_i18n_";
const dynamicCache: Record<string, Record<string, string>> = {};

function getCachedTranslations(langCode: string): Record<string, string> | null {
  if (dynamicCache[langCode]) return dynamicCache[langCode];
  try {
    const stored = localStorage.getItem(CACHE_PREFIX + langCode);
    if (stored) {
      const parsed = JSON.parse(stored);
      dynamicCache[langCode] = parsed;
      return parsed;
    }
  } catch {}
  return null;
}

function saveCachedTranslations(langCode: string, data: Record<string, string>) {
  dynamicCache[langCode] = data;
  try {
    localStorage.setItem(CACHE_PREFIX + langCode, JSON.stringify(data));
  } catch {}
}

let translatingLangs = new Set<string>();

/**
 * Translates all UI keys to a given language via OpenAI and caches the result.
 * Called once per language, results persist in localStorage.
 */
export async function translateUIToLanguage(langCode: string, langLabel: string): Promise<boolean> {
  if (translations[langCode as LanguageCode]) return true; // already built-in
  if (getCachedTranslations(langCode)) return true; // already cached
  if (translatingLangs.has(langCode)) return false; // already in progress

  translatingLangs.add(langCode);

  try {
    const enKeys = translations["en"];
    const allKeys = Object.keys(enKeys) as TranslationKey[];

    // Split into chunks of ~40 keys to avoid token limits
    const chunkSize = 40;
    const result: Record<string, string> = {};

    const { translateUIChunk } = await import("./openai");

    for (let i = 0; i < allKeys.length; i += chunkSize) {
      const chunk = allKeys.slice(i, i + chunkSize);
      const sourceObj: Record<string, string> = {};
      for (const k of chunk) sourceObj[k] = enKeys[k];

      const parsed = await translateUIChunk(sourceObj, langCode, langLabel);
      Object.assign(result, parsed);
    }

    saveCachedTranslations(langCode, result);
    return true;
  } catch (e) {
    console.error(`Failed to translate UI to ${langCode}:`, e);
    return false;
  } finally {
    translatingLangs.delete(langCode);
  }
}

/**
 * Hook to auto-translate UI when a non-built-in language is selected.
 * Call this in App or a top-level component.
 */
export function useAutoTranslateUI(uiLanguage: string, onComplete?: () => void) {
  if (translations[uiLanguage as LanguageCode]) return; // built-in, nothing to do
  if (getCachedTranslations(uiLanguage)) return; // already cached

  // Find language label
  import("./languages").then(({ LANGUAGES }) => {
    const lang = LANGUAGES.find((l: any) => l.code === uiLanguage);
    const label = lang?.label || uiLanguage;
    translateUIToLanguage(uiLanguage, label).then((ok) => {
      if (ok && onComplete) onComplete();
    });
  });
}

export function useTranslation(uiLanguage: string) {
  // Check built-in translations first
  if (translations[uiLanguage as LanguageCode]) {
    const lang = uiLanguage as LanguageCode;
    return (key: TranslationKey) => {
      return translations[lang][key] || translations["en"][key] || key;
    };
  }

  // Check dynamic cache (localStorage)
  const cached = getCachedTranslations(uiLanguage);
  if (cached) {
    return (key: TranslationKey) => {
      return cached[key] || translations["en"][key] || key;
    };
  }

  // Fallback to English (translation will be triggered separately)
  return (key: TranslationKey) => {
    return translations["en"][key] || key;
  };
}
